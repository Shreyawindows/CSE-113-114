#include<stdio.h>
int main()
{
    switch(char){
    case 'a':
        printf("a is vowel");
        case 'A':
            printf("A is vowel");
        case 'e':
            printf("e is vowel");
        case 'E':
            printf("E is vowel");
        case 'i':
            printf("i is vowel");
        case 'I':
            printf("I is vowel");
        case 'o':
            printf("o is vowel");
        case 'O':
            printf("O is vowel");
        case 'u':
            printf("u is vowel");
        case 'U':
            printf("U is vowel");
            printf("\n%c is a vowel",char);
        default:
            printf("\n%c is not a vowel",char);

    }
}
